#include <unistd.h>

void	ft_putnbr(int in)
{
	char	str[10];
	int		k;
	int		pos;

	k = 0;
	pos = 1;
	if (in < 0)
	{
		pos = 0;
		in *= -1;
	}
	while (!(in < 10))
	{
		str[k] = (in % 10) + 48;
		in -= in % 10;
		in /= 10;
		k++;
	}
	str[k] = in + 48;
	if (pos == 0)
		write(1, "-", 1);
	while (k != -1)
	{
		write(1, &str[k], 1);
		k--;
	}
}
