#include "libft.h"

void	myputnbr(int in)
{
	char	str[10];
	int		k;

	k = 0;
	(in < 0) ? ft_putchar('-') : in;
	while (!((in > -10) && (in < 10)))
	{
		str[k++] = ft_abs(in % 10) + 48;
		in /= 10;
	}
	str[k] = ft_abs(in) + 48;
	while (k > -1)
		ft_putchar(str[k--]);
}
