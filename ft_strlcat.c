#include "libft.h"

size_t	ft_strlcat(char *d, const char *s, size_t n)
{
	int			i;
	int			j;

	i = 0;
	while ((d[i] != '\0') && (i < (int) n))
		i++;
	j = 0;
	while ((s[j] != '\0') && (i + 1 < (int) n))
	{
		d[i] = s[j];
		i++;
		j++;
	}
	if (i - j < (int) n)
		d[i] = '\0';
	return (i - j + ft_strlen(s));

}
