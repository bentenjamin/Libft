int	ft_atoi(char *str)
{
	int	i;
	int	k;
	int	neg;

	k = 0;
	neg = 1;
	while (!((str[k] >= '0') && (str[k] <= '9')))
		k++;
	if (k != 0)
		if (str[k - 1] == '-')
			neg = -1;
	i = 0;
	while (str[k] != '\0' && (str[k] >= '0') && (str[k] <= '9'))
	{
		i *= 10;
		i += str[k] - 48;
		k++;
	}
	i *= neg;
	return (i);
}
