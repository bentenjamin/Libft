#include "libft.h"

int	ft_match(char *s1, char *s2)
{
	int i;
	int k;

	i = 0;
	k = 0;
	while(s1[i] && s2[k])
	{
		if (s1[i] != s2[k])
		{
			if (s2[k] == '*')
			{
				k++;
				while (s1[i] && (s1[i] != s2[k]))
					i++;
				return ((!s1[i] && (s1[i] != s2[k])) ? 0 : match(s1 + i, s2 + k) );
			}
			return (0);
		}
		i++;
		k++;
	}
	return ((s1[i] == s2[k]));
}