#include "libft.h"

char	*ft_strcat(char *d, const char *src)
{
	int			i;
	int			j;
	const char	*s;

	i = ft_strlen(d);
	s = src;
	j = 0;
	while (s[j] != '\0')
		d[i++] = s[j++];
	d[i] = '\0';
	return (d);
}
